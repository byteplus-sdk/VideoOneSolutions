package util

import "hash/fnv"

func StringInSlice(a string, list []string) bool {
	for _, b := range list {
		if b == a {
			return true
		}
	}
	return false
}

func IntInSlice(a int, list []int) bool {
	for _, b := range list {
		if b == a {
			return true
		}
	}
	return false
}

func Hashcode(s string) int64 {
	h := fnv.New32()
	h.Write([]byte(s)) //nolint
	hashValue := h.Sum32()
	return int64(hashValue)
}

func Bool2Int(b bool) int {
	if b {
		return 1
	}
	return 0
}
